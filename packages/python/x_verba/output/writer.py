    def _render_summary_yaml(self, summary: dict) -> str:
        """Render plain-language executive summary."""
        ai_count = summary.get("ai_integrations_detected", 0)
        critical = summary.get("critical", 0)
        high = summary.get("high", 0)
        coverage = summary.get("governance_coverage", "0%")
        gamma = summary.get("structural_gamma", 0)
        files = summary.get("files_scanned", 0)

        if critical > 0:
            risk_level = "HIGH — this system has critical governance gaps"
        elif high > 0:
            risk_level = "MEDIUM — this system has high-severity gaps"
        else:
            risk_level = "LOW — no critical or high governance gaps detected"

        data = {
            "executive_summary": {
                "files_scanned": files,
                "ai_integrations_detected": ai_count,
                "governance_coverage": coverage,
                "structural_gamma_proxy": gamma,
                "risk_level": risk_level,
                "plain_language": (
                    f"X-Verba scanned {files} files and found "
                    f"{ai_count} AI integration points. "
                    f"{critical} critical and {high} high-severity "
                    f"governance gaps were detected. "
                    f"Governance coverage is currently {coverage}. "
                    f"The structural Gamma proxy is {gamma} — "
                    f"{'below' if gamma < 1.0 else 'at or above'} "
                    f"the governance sufficiency threshold of 1.0."
                )
            }
        }
        return yaml.dump(data, default_flow_style=False, allow_unicode=True)

