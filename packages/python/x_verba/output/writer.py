    def _render_thresholds_yaml(self, primitives: dict) -> str:
        """Render detected thresholds and boundary conditions."""
        ai_integrations = primitives.get("ai_integrations", [])
        threshold_data = []

        for i, ai in enumerate(ai_integrations, 1):
            temp = ai.get("temperature")
            max_tokens = ai.get("max_tokens")

            if temp is not None or max_tokens is not None:
                threshold_data.append({
                    f"TH-{i:03d}": {
                        "location": ai["location"],
                        "plain_english": (
                            "Model parameters detected at this AI call. "
                            "These parameters directly affect output "
                            "variability and must be governed explicitly."
                        ),
                        "detected_parameters": {
                            "temperature": temp,
                            "temperature_assessment": (
                                f"HIGH — temperature {temp} increases output "
                                f"variability significantly. Governance cost "
                                f"increases with higher temperature."
                                if temp and temp > 0.7
                                else f"Within acceptable range ({temp})"
                                if temp else "Not detected"
                            ),
                            "max_tokens": max_tokens,
                            "max_tokens_assessment": (
                                "No explicit output length limit detected. "
                                "Unbounded output length is a governance gap."
                                if not max_tokens
                                else f"Output length limited to {max_tokens} tokens"
                            ),
                        },
                        "recommended_thresholds": {
                            "maximum_temperature": 0.3,
                            "temperature_note": (
                                "Lower temperature = more deterministic output = "
                                "easier to govern. For high-stakes contexts, "
                                "temperature should not exceed 0.3."
                            ),
                            "maximum_output_tokens": None,
                            "minimum_confidence_score": None,
                            "forbidden_content_patterns": None,
                        },
                    }
                })

        data = {"thresholds": threshold_data if threshold_data else [
            {
                "note": (
                    "No AI model parameters detected. "
                    "Define your Thresholds below."
                )
            }
        ]}
        return yaml.dump(data, default_flow_style=False, allow_unicode=True)

