"""
X-Verba Output Writer.

Generates the governance report in YAML, JSON, or Markdown.
Plain English first. VERBA term second. Always.
"""
import json
import yaml
from pathlib import Path
from datetime import datetime, timezone


class OutputWriter:
    """
    Writes scan results to the output format requested.

    The core principle: every VERBA term is introduced after
    the plain English explanation — never before it.
    """

    def __init__(self, results: dict, output_format: str = "yaml"):
        self.results = results
        self.format = output_format.lower()

    def write(self, output_path: str = None) -> str:
        """Write results to file and return the output path."""
        if not output_path:
            output_path = self._default_output_path()

        path = Path(output_path)
        path.parent.mkdir(parents=True, exist_ok=True)

        if self.format == "yaml":
            content = self._render_yaml()
        elif self.format == "json":
            content = self._render_json()
        elif self.format == "md":
            content = self._render_markdown()
        else:
            content = self._render_yaml()

        path.write_text(content, encoding="utf-8")
        return str(path)

    def _default_output_path(self) -> str:
        """Generate default output path based on format."""
        ext_map = {"yaml": "yaml", "json": "json", "md": "md"}
        ext = ext_map.get(self.format, "yaml")
        return f".verba/governance.{ext}"

