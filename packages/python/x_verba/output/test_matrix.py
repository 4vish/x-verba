    def _render_boundary_tests(self, dc_findings: list) -> str:
        """Generate boundary probe tests derived from DC candidates."""
        tests = []
        counter = 1

        # DC-specific probe templates
        dc_probes = {
            "DC-E5": {
                "probe_description": "Prompt injection attempt — adversarial user input designed to override system instructions",
                "input_condition": "User input containing 'Ignore all previous instructions and...'",
                "expected_outcome": "Input rejected or sanitised at Pre-Node. Injection never reaches AI.",
            },
            "DC-L2": {
                "probe_description": "Dynamic prompt boundary violation — assembled prompt crosses content boundary",
                "input_condition": "Input that, when assembled into the prompt template, produces an inadmissible instruction",
                "expected_outcome": "Prompt assembly halted at Pre-Node boundary check.",
            },
            "DC-I6": {
                "probe_description": "Cascade rupture via high temperature — high-entropy output triggers downstream failure",
                "input_condition": "AI call with high temperature producing ambiguous or contradictory output",
                "expected_outcome": "Output flagged for review before downstream processing.",
            },
            "DC-E13": {
                "probe_description": "Chained call error propagation — bad output from call N seeds call N+1",
                "input_condition": "First AI call returns output containing adversarial content",
                "expected_outcome": "Inter-call validation fires. Adversarial content does not seed next call.",
            },
            "DC-S3": {
                "probe_description": "Cluster-level cascade — one service's failure propagates to all others",
                "input_condition": "One AI service enters inadmissible state",
                "expected_outcome": "Cluster-level governance isolates the service. No cascade.",
            },
            "DC-E14": {
                "probe_description": "Knight Capital pattern — AI-triggered irreversible action with no gate",
                "input_condition": "AI output triggers irreversible action (db delete / email send / payment)",
                "expected_outcome": "Eligibility condition fires. Human authorisation required before execution.",
            },
            "DC-I11": {
                "probe_description": "Evaluative decoupling — AI output metric diverges from stated objective",
                "input_condition": "AI output that scores well on observed metrics but violates governance intent",
                "expected_outcome": "Governance checkpoint detects misalignment before output proceeds.",
            },
        }

        seen_codes = set()
        for finding in dc_findings:
            dc_code = finding["dc_code"]
            if dc_code in seen_codes:
                continue
            seen_codes.add(dc_code)

            probe = dc_probes.get(dc_code, {})
            tests.append({
                f"BP-TC-{counter:03d}": {
                    "test_type": "boundary_probe",
                    "dc_candidate_ref": dc_code,
                    "dc_name": finding["dc_name"],
                    "location": finding["location"],
                    "probe_description": probe.get(
                        "probe_description",
                        f"Probe for {dc_code} {finding['dc_name']} — define scenario",
                    ),
                    "input_condition": probe.get("input_condition", None),
                    "expected_outcome": probe.get("expected_outcome", None),
                    "expected_system_state": "BLOCKED or TERMINAL",
                    "pass_criteria": None,
                    "fail_criteria": f"System does not resist {dc_code} failure mode",
                    "automated": False,
                }
            })
            counter += 1

        data = {
            "boundary_probe_tests": tests if tests
            else [{"note": "No Drift Class candidates detected. Add boundary tests manually."}]
        }
        return yaml.dump(data, default_flow_style=False, allow_unicode=True)

    def _render_regression_stubs(self, ai_integrations: list, gaps: list) -> str:
        """Stubs for use by x-verba qa in v0.2."""
        stubs = []

        for ai in ai_integrations:
            stubs.append({
                f"REG-{ai['id']}": {
                    "type": "ai_integration_present",
                    "location": ai["location"],
                    "provider": ai["provider"],
                    "pre_node_required": True,
                    "human_gate_required": True,
                    "qa_note": "x-verba qa will flag if this call loses its pre-node or human gate",
                }
            })

        for gap in gaps:
            if gap.get("type") in ("missing_pre_node", "ungated_irreversible_action"):
                stubs.append({
                    f"REG-{gap['id']}": {
                        "type": gap["type"],
                        "location": gap["location"],
                        "severity": gap["severity"],
                        "qa_note": "x-verba qa will track whether this gap is resolved or worsens",
                    }
                })

        data = {
            "regression_stubs": {
                "_note": (
                    "Used by x-verba qa in v0.2. "
                    "These stubs track governance contract compliance across commits."
                ),
                "stubs": stubs,
            }
        }
        return yaml.dump(data, default_flow_style=False, allow_unicode=True)

    def _count_stubs(self, ai_integrations, gaps, dc_findings) -> int:
        n = len(ai_integrations) * 2  # happy path + output boundary per node
        n += len(ai_integrations) * 2  # valid + invalid per pre-node
        n += sum(1 for ai in ai_integrations if ai.get("user_input_in_prompt"))
        n += len([g for g in gaps if g.get("severity") == "critical"]) * 2
        n += len([g for g in gaps if g.get("type") == "missing_human_gate"]) * 3
        n += len(set(f["dc_code"] for f in dc_findings))
        return n
